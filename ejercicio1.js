// 1. Contar del 1 al 100 de uno en uno


//  for 
console.log("for");
for (let i = 1; i <= 100; i++) {
  console.log(i);
}

//  while 
console.log("while");
let i = 1;
while (i <= 100) {
  console.log(i);
  i++;
}

// do...while 
console.log("do...while");
let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 100);


